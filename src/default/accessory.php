        <h2 class="p-4 text-wrap">Assessoria e Consultoria Jurídica</h1>

            <p>Nas áreas em que atuo, direito de Família, Sucessões e Previdenciário, ofereço os serviços de assessoria e consultoria jurídica.</p>

            <p>A Consultoria Jurídica, tem como propósito a PREVENÇÃO que visa o aconselhamento ao cliente para esclarecer questões ligadas ao universo do Direito e prestar informações e opiniões especializadas com base na demanda solicitada.</p>

            <p>A Assessoria Jurídica, possui como objetivo a atuação PREVENTIVA CONTÍNUA buscando soluções com respaldo legal evitando o litígio ao cliente.</p>

            <p>Cito como exemplos:</p>

            <ul>

                <li>O momento anterior ao casamento ou de constituir qualquer união, importante ter uma orientação jurídica acerca do regime de bens a ser escolhido pelo casal, e se será necessário realizar um pacto antenupcial;</li>

                <li>No caso de um benefício previdenciário, por vezes é necessário realizar uma contagem de tempo de contribuição, inclusive uma análise para verificar se não possui irregularidades nas contribuições junto ao INSS;</li>

                <li>E um último exemplo, nos casos de testamento, ter uma correta orientação acerca dos tipos de testamentos, o que pode ou não dentro dos limites da lei ser deixado aos herdeiros ou terceiros.</li>

            </ul>

